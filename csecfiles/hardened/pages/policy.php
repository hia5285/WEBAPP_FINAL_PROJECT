<h1>Acceptable Use Policy</h1>
<p>
    All documents uploaded to CSecFiles are considered company property.
    Do not upload personal data, credentials, or material unrelated to your
    work. Confidential documents must use the <em>private</em> visibility
    setting and only the <em>department</em> setting when intended for
    sharing inside your team.
</p>
<p class="muted">
    Violations of this policy will be reviewed by the IT and HR teams.
</p>
