<h1>About CSecFiles</h1>
<p>
    CSecFiles is the company's internal document collaboration platform.
    Employees can upload, organise, search and discuss documents within their
    team or department.
</p>
<p class="muted">
    This particular build is the <strong>hardened</strong> classroom edition
    used in the Web Security course. The same five vulnerabilities present in
    the deliberately-vulnerable build are mitigated here.
</p>
